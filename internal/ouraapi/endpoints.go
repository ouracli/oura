// DefaultDateWindow returns the default start_date/end_date for date-range
// endpoints: 7 days ago through TOMORROW. Tomorrow — not today — because the
// live API treats end_date as exclusive on several endpoints (probed
// 2026-07-07: daily_activity, sleep periods, and workouts, which filter on
// the workout's UTC start time): with end_date=today those endpoints
// silently omit today's documents, including last night's sleep. The
// midnight-anchored daily endpoints are end-inclusive, so the extra day is
// harmless there — tomorrow's documents do not exist yet.
func DefaultDateWindow(now time.Time) (start, end string) {
	const layout = "2006-01-02"
	return now.AddDate(0, 0, -7).Format(layout), now.AddDate(0, 0, 1).Format(layout)
}

// The endpoint registry is the single source of truth for ouracli. The cobra
// data commands, the MCP tool list, and the `oura schema` manifest are all
// generated by iterating Endpoints, so the three surfaces cannot drift.

// Style classifies an endpoint's query-parameter shape and response envelope.
type Style int

const (
	// StyleDateRange: start_date/end_date (YYYY-MM-DD) + next_token, and
	// usually a fields projection; response is {"data":[...],"next_token"}.
	StyleDateRange Style = iota
	// StyleDatetimeRange: start_datetime/end_datetime (RFC3339) + latest +
	// next_token; time-series data with no per-document routes.
	StyleDatetimeRange
	// StyleTokenOnly: next_token + fields only, no date window
	// (ring_configuration).
	StyleTokenOnly
	// StyleSingleObject: no query params, and a bare JSON object response
	// rather than a data/next_token envelope (personal_info).
	StyleSingleObject
)

// Endpoint describes one Oura v2 usercollection resource.
type Endpoint struct {
	CLI        string // CLI command name, e.g. "sleep"
	MCPTool    string // MCP tool name, e.g. "oura_sleep"
	Path       string // API path, e.g. "/usercollection/daily_sleep"
	Short      string // one-line description of what the data is
	Style      Style
	HasDocID   bool // GET .../{document_id} exists
	HasFields  bool // the fields query param actually projects (see Fields)
	Sandbox    bool // available under /v2/sandbox
	Deprecated bool
	// Fields is every top-level field name in this endpoint's documents —
	// the valid values for the fields projection when HasFields is true, and
	// shape documentation otherwise. Sourced from OpenAPI 1.35 and verified
	// against live responses (2026-07-07); where the two disagreed
	// (heartrate/ring_battery_level: the spec's timestamp_unix does not
	// exist, the API returns producer_timestamp) the live shape wins.
	// Sorted, so lookups can binary-search and diffs stay minimal.
	//
	// The Oura API silently ignores unknown names in a fields projection and
	// returns FULL documents when nothing valid remains, so a typo'd
	// projection succeeds with the wrong (much larger) payload. That is why
	// callers validate against this list client-side (NormalizeFields)
	// instead of trusting the server to complain.
	Fields []string
}

