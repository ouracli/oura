// FindEndpoint returns the endpoint registered under the given CLI name.
func FindEndpoint(cli string) (Endpoint, bool) {
	for _, ep := range Endpoints {
		if ep.CLI == cli {
			return ep, true
		}
	}
	return Endpoint{}, false
}

// NormalizeFields validates a comma-separated fields projection against
// ep.Fields and returns it normalized (whitespace trimmed, empty items
// dropped). The Oura API silently ignores unknown field names — and falls
// back to FULL documents when none are valid — so a typo would otherwise
// succeed with the wrong payload; this is the only place a bad projection
// can be caught. An empty projection normalizes to "". Requesting fields on
// an endpoint where they do not project (HasFields false) is a usage error.
func (ep Endpoint) NormalizeFields(fields string) (string, error) {
	if strings.TrimSpace(fields) == "" {
		return "", nil
	}
	if !ep.HasFields {
		return "", envelope.New(envelope.KindUsage, "fields_not_supported",
			ep.CLI+" does not support the fields projection",
			"drop the fields argument; "+ep.CLI+" always returns full documents")
	}
	var out []string
	for _, f := range strings.Split(fields, ",") {
		f = strings.TrimSpace(f)
		if f == "" {
			continue
		}
		if !ep.HasField(f) {
			return "", envelope.New(envelope.KindUsage, "unknown_field",
				"unknown field "+f+" for "+ep.CLI+" (Oura would silently ignore it)",
				"valid fields: "+strings.Join(ep.Fields, ", "))
		}
		out = append(out, f)
	}
	if len(out) == 0 {
		return "", nil
	}
	return strings.Join(out, ","), nil
}

// HasField reports whether name is a top-level field of ep's documents.
func (ep Endpoint) HasField(name string) bool {
	for _, f := range ep.Fields {
		if f == name {
			return true
		}
	}
	return false
}

// List fetches one page of ep's collection with the caller-built query (start
// date, next_token, fields, etc.) and returns the {"data",...,"next_token"}
// envelope. The caller owns the query so it can honor ep.Style.
func (c *Client) List(ctx context.Context, ep Endpoint, q url.Values) (*ListResponse, error) {
	if ep.Style == StyleSingleObject {
		return nil, envelope.New(envelope.KindUsage, "not_listable",
			ep.CLI+" returns a single object, not a collection",
			"call the endpoint without --start/--end/--all")
	}
	var lr ListResponse
	if err := c.Get(ctx, ep.Path, q, &lr); err != nil {
		return nil, err
	}
	return &lr, nil
}

// Doc fetches a single document by id from ep's collection. When ep supports
// the fields projection and fields is non-empty, it is passed through.
func (c *Client) Doc(ctx context.Context, ep Endpoint, id, fields string) (json.RawMessage, error) {
	if !ep.HasDocID {
		return nil, envelope.New(envelope.KindUsage, "no_document_route",
			ep.CLI+" has no per-document endpoint",
			"fetch the collection instead and filter client-side")
	}
	q := url.Values{}
	norm, err := ep.NormalizeFields(fields)
	if err != nil {
		return nil, err
	}
	if norm != "" {
		q.Set("fields", norm)
	}
	var raw json.RawMessage
	if err := c.Get(ctx, ep.Path+"/"+id, q, &raw); err != nil {
		return nil, err
	}
	return raw, nil
}

// Object fetches ep's bare single-object response (personal_info), returned as
// raw JSON.
func (c *Client) Object(ctx context.Context, ep Endpoint) (json.RawMessage, error) {
	if ep.Style != StyleSingleObject {
		return nil, envelope.New(envelope.KindUsage, "not_single_object",
			ep.CLI+" returns a collection, not a single object",
			"use the list form with --start/--end")
	}
	var raw json.RawMessage
	if err := c.Get(ctx, ep.Path, nil, &raw); err != nil {
		return nil, err
	}
	return raw, nil
}

// ListAll follows next_token pagination from the caller-built starting query,
// invoking emit once per document across every page until next_token is null.
// A non-nil error from emit stops the walk and is returned.
func (c *Client) ListAll(ctx context.Context, ep Endpoint, q url.Values, emit func(json.RawMessage) error) error {
	// Copy so we can mutate next_token without clobbering the caller's values.
	page := url.Values{}
	for k, vs := range q {
		page[k] = append([]string(nil), vs...)
	}
	for {
		lr, err := c.List(ctx, ep, page)
		if err != nil {
			return err
		}
		for _, doc := range lr.Data {
			if err := emit(doc); err != nil {
				return err
			}
		}
		if lr.NextToken == nil || *lr.NextToken == "" {
			return nil
		}
		page.Set("next_token", *lr.NextToken)
	}
}
